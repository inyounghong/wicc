              <p>
                Eva Tardos is the Jacob Gould Schurman Professor of Computer Science and Information Science at Cornell. Her research focuses on Algorithm Design and the interface of Computing and Economics, with an emphasis on algorithms and games on networks. She is a member of the National Academy of Engineering and National Academy of Sciences. Tardos received her PhD in from Eötvös University, Budapest, Hungary. She did a series of postdocs and short term positions in computer science including time spent at Berkeley and MIT, and has been a faculty member at Cornell since 1989. 
              </p><br>
              <p>
                She has been the faculty advisor for the Women in Computing at Cornell since its inception, and though WICC’s helped encourage many more women to choose CS major. She earned my PhD in 1984, the year that had most women in computing (see for example, the charts in <a href="http://www.npr.org/sections/money/2014/10/21/357629765/when-women-stopped-coding" target="_blank">this</a> article for trends in percentage of women in computing). With the help of WICC, the percentage of women in CS at Cornell grew to currently over 25% from barely 10% not so many years ago. She is very proud of WICC for all the group has done to make CS a better major for women, and committed to continue this work, hoping to bring the percentage of women in CS at Cornell up to at least what it was nationally in the early eighties.
